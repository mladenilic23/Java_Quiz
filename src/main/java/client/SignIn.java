    private void buttonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonLoginActionPerformed
        // TODO add your handling code here:
        String username = this.textUsername.getText();
        String password = String.valueOf(this.textPassword.getPassword());
    
        if (!username.isEmpty() && !password.isEmpty()) 
        {
            String role = this.comboRole.getSelectedItem().toString();
            //Ako je uloga "contestant", ostavi je kako jeste, inaƒçe koristi ulogu "admin"
            if (role.equals("contestant")) {
                role = "contestant";
            }
                    
            String loginMessage = username + ":" + password + ":" + role;
            
            client.login(loginMessage, role, this.textUsername.getText());
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Insert username and password!");
        }
    }//GEN-LAST:event_buttonLoginActionPerformed

